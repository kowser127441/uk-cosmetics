		<h2>What is a cookie? </h2>

		<p>Cookies are text files

			containing small amounts of information which are downloaded to your device

			when you visit a website. Cookies are then sent back to the originating website

			on each subsequent visit, or to another website that recognises that cookie.

			Cookies are useful because they allow a website to recognise a user's device.

			You can find more information about cookies at: <a

href="http://www.allaboutcookies.org/">www.allaboutcookies.org</a> and <a href="http://www.youronlinechoices.eu/">www.youronlinechoices.eu</a> or for a video about cookies visit <a

href="http://www.google.co.uk/goodtoknow/data-on-the-web/cookies">www.google.co.uk/goodtoknow/data-on-the-web/cookies</a>. </p>

		<p>Cookies do lots of

			different jobs, like letting you navigate between pages efficiently,

			remembering your preferences, and generally improving the user experience. Cookies may be session cookies, i.e. only

			stored on your device until you close the browser window or persistent

			cookies. Persistent cookies remain on

			your device even after the web browser is closed and are reactivated each time

			you revisit the particular site that served the cookie. </p>

